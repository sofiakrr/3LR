alert("Программа для нахождения среднего геометрического, то есть квадратный корень из их произведения");
a = prompt("Введите неотрицательное число а");
a = parseFloat(a);
b = prompt("Введите неотрицательное число b");
b = parseFloat(b);
var result = Math.sqrt(a*b);
alert("Результат нахождения среднего геометрического: " + result)
